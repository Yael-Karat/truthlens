def analyze_text_with_ai(text):
    """
    גרסת דמו של ניתוח טקסט — ללא שימוש ב־OpenAI.
    מזהה טענות שקריות ידועות ומחזיר תגובה מותאמת עם סמלי אזהרה.
    אם לא זוהתה טענה מוכרת, מחזיר הודעה כללית עם סימן חיובי.
    """

    known_false_claims = {
        "החיסון גורם לאוטיזם": "❌ הטענה שמחסנים גורמים לאוטיזם הופרכה על ידי מחקרים רבים. מדובר בטענה מטעה.",
        "העולם שטוח": "❌ מדובר בתיאוריית קונספירציה – הוכח מדעית שהעולם כדורי.",
        "5G מפיץ קורונה": "❌ אין כל ראיה מדעית לקשר בין טכנולוגיית 5G להתפשטות וירוס הקורונה.",
        "הנאצים הקימו את ויקיפדיה": "❌ מדובר בטענה מופרכת ושקרית – ויקיפדיה נוסדה ב־2001, עשרות שנים אחרי מלחמת העולם השנייה."
    }

    for claim, response in known_false_claims.items():
        if claim in text:
            return response

    # ברירת מחדל אם לא נמצא טקסט מוכר
    return "✅ לא זוהתה בעיה מובהקת בטקסט, אך תמיד מומלץ לבדוק מקורות אמינים."
